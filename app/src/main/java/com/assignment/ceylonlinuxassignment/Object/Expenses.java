package com.assignment.ceylonlinuxassignment.Object;
public class Expenses {

    private String expenses_type = "";
    private String expenses_amount = "";
    private String expenses_image = "";
    private String expenses_date = "";
    private String expenses_add_date = "";
    private String expenses_add_time = "";

    public String getExpenses_type() {
        return expenses_type;
    }

    public void setExpenses_type(String expenses_type) {
        this.expenses_type = expenses_type;
    }

    public String getExpenses_amount() {
        return expenses_amount;
    }

    public void setExpenses_amount(String expenses_amount) {
        this.expenses_amount = expenses_amount;
    }

    public String getExpenses_image() {
        return expenses_image;
    }

    public void setExpenses_image(String expenses_image) {
        this.expenses_image = expenses_image;
    }

    public String getExpenses_date() {
        return expenses_date;
    }

    public void setExpenses_date(String expenses_date) {
        this.expenses_date = expenses_date;
    }

    public String getExpenses_add_date() {
        return expenses_add_date;
    }

    public void setExpenses_add_date(String expenses_add_date) {
        this.expenses_add_date = expenses_add_date;
    }

    public String getExpenses_add_time() {
        return expenses_add_time;
    }

    public void setExpenses_add_time(String expenses_add_time) {
        this.expenses_add_time = expenses_add_time;
    }
}
